## Distribución de PageRank Interno: Cómo Fluye la Autoridad

El PageRank interno funciona como el agua: fluye desde las páginas con más autoridad hacia las páginas que estas enlazan. Para un flujo de autoridad efectivo debes:

### Regla de los 3 Clics
El contenido más importante de tu web debe estar a **máximo 3 clics de distancia** desde la página principal (homepage). Cuanto más lejos esté una página en la arquitectura, menos autoridad interna recibe y menos probable es que rankee bien.

### Planifica la Jerarquía de Autoridad

- **Paso 1:** Identifica el contenido clave (home, pilares muy enlazados externamente).
- **Paso 2:** Desde esas páginas, enlaza directamente a tu contenido más importante de nivel inferior.
- **Paso 3:** Evita crear "cuellos de botella" donde toda la autoridad queda atrapada en las categorías sin llegar a los artículos.

### Planifica los enlaces internos antes de publicar
Antes de publicar un artículo nuevo, ten una lista de al menos 3-5 páginas existentes donde tendrá sentido agregar un enlace hacia el nuevo contenido. Publicar sin plan de enlazado es publicar a ciegas.

